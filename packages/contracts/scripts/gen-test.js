  '        vault,',
  '        "AccessControlUnauthorizedAccount"',
  '      );',
  '    });',
  '',
  '    it("should not allow non-operator to trigger yield rebalance", async function () {',
  '      await expect(',
  '        vault.connect(user).rebalanceYield(100)',
