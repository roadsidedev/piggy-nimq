  '',
  '    it("should have zero initial balance", async function () {',
  '      expect(await vault.totalAssets()).to.equal(0);',
  '    });',
  '',
  '    it("should revert when initializing twice (UUPS)", async function () {',
  '      const PiggyVaultFactory = await ethers.getContractFactory("PiggyVault");',
  '      await expect(',
  '        upgrades.upgradeProxy(await vault.getAddress(), PiggyVaultFactory, {',
  '          kind: "uups",',
  '          call: { fn: "initialize", args: [await asset.getAddress(), admin.address] },',
  '        })',
  '      ).to.be.revertedWith("Initializable: contract is already initialized");',
  '    });',
  '  });',
  '',
  '  describe("Deposits", function () {',
  '    it("should allow deposits", async function () {',
  '      const amount = ethers.parseUnits("1000", 6);',
  '      await asset.connect(user).approve(await vault.getAddress(), amount);',
  '      await expect(() =>',
  '        vault.connect(user).deposit(amount, user.address)',
  '      ).to.changeTokenBalance(asset, vault, amount);',
  '    });',
  '',
  '    it("should mint correct internal shares", async function () {',
  '      const amount = ethers.parseUnits("500", 6);',
  '      await asset.connect(user).approve(await vault.getAddress(), amount);',
  '      await vault.connect(user).deposit(amount, user.address);',
  '      expect(await vault.internalShares(user.address)).to.equal(amount);',
  '    });',
  '',
  '    it("should revert deposit with zero amount", async function () {',
