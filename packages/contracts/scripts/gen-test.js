  '      await vault.connect(borrower).borrow(borrowAmount, borrower.address);',
  '      const repayAmount = ethers.parseUnits("2000", 6);',
  '      await asset.connect(borrower).approve(await vault.getAddress(), repayAmount);',
  '      await vault.connect(borrower).repay(repayAmount, borrower.address);',
  '    });',
  '',
  '    it("should revert borrow with zero amount", async function () {',
  '      await expect(',
  '        vault.connect(borrower).borrow(0, borrower.address)',
  '      ).to.be.revertedWithCustomError(vault, "ZeroAmount");',
  '    });',
  '  });',
  '',
  '  describe("Access Control", function () {',
  '    it("should not allow non-admin to set adapter", async function () {',
  '      await expect(',
  '        vault.connect(user).setYieldAdapter(ethers.ZeroAddress)',
  '      ).to.be.revertedWithCustomError(',
  '        vault,',
  '        "AccessControlUnauthorizedAccount"',
  '      );',
  '    });',
  '',
  '    it("should not allow non-admin to set total deposit cap", async function () {',
  '      await expect(',
  '        vault.connect(user).setTotalDepositCap(1000)',
