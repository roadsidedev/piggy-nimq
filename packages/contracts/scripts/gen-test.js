  '    });',
  '  });',
  '',
  '  describe("enableYield", function () {',
  '    it("should allow user to enable yield", async function () {',
  '      const amount = ethers.parseUnits("1000", 6);',
  '      await asset.connect(user).approve(await vault.getAddress(), amount);',
  '      await vault.connect(user).deposit(amount, user.address);',
  '      await vault.connect(user).enableYield(amount);',
  '      const DEAD_SHARES = 1000n;',
  '      expect(await vault.supplyShares(user.address)).to.equal(amount - DEAD_SHARES);',
  '      expect(await vault.supplyShares(ethers.ZeroAddress)).to.equal(DEAD_SHARES);',
  '      expect(await vault.yieldEnabled(user.address)).to.be.true;',
  '    });',
  '',
  '    it("should revert enableYield when not deposited", async function () {',
  '      await expect(',
  '        vault.connect(other).enableYield(100)',
  '      ).to.be.revertedWithCustomError(vault, "InsufficientBalance");',
  '    });',
  '',
  '    it("should revert enableYield with zero amount", async function () {',
  '      await expect(',
  '        vault.connect(other).enableYield(0)',
  '      ).to.be.revertedWithCustomError(vault, "ZeroAmount");',
  '    });',
  '',
  '    it("should revert when enabling yield twice", async function () {',
  '      const amount = ethers.parseUnits("1000", 6);',
  '      await asset.connect(user).approve(await vault.getAddress(), amount);',
  '      await vault.connect(user).deposit(amount, user.address);',
  '      await vault.connect(user).enableYield(amount);',
  '      await expect(',
  '        vault.connect(user).enableYield(100)',
  '      ).to.be.revertedWithCustomError(vault, "YieldAlreadyEnabled");',
  '    });',
  '  });',
  '',
  '  describe("disableAllYield", function () {',
  '    it("should disable yield and return funds", async function () {',
  '      const amount = ethers.parseUnits("1000", 6);',
  '      await asset.connect(user).approve(await vault.getAddress(), amount);',
  '      await vault.connect(user).deposit(amount, user.address);',
  '      await vault.connect(user).enableYield(amount);',
  '      await mockAdapter.setSuppliedValue(amount);',
  '      await vault.connect(user).disableAllYield();',
  '      expect(await vault.yieldEnabled(user.address)).to.be.false;',
  '      expect(await vault.supplyShares(user.address)).to.equal(0);',
  '      expect(await vault.internalShares(user.address)).to.be.gt(0);',
  '    });',
  '',
  '    it("should revert when yield not enabled", async function () {',
  '      await expect(',
  '        vault.connect(other).disableAllYield()',
  '      ).to.be.revertedWithCustomError(vault, "NoYieldPosition");',
  '    });',
  '  });',
  '',
  '  describe("disableYield", function () {',
  '    it("should disable partial yield", async function () {',
  '      const amount = ethers.parseUnits("1000", 6);',
  '      await asset.connect(user).approve(await vault.getAddress(), amount);',
  '      await vault.connect(user).deposit(amount, user.address);',
  '      await vault.connect(user).enableYield(amount);',
  '      await mockAdapter.setSuppliedValue(amount);',
  '      const halfAmount = amount / 2n;',
  '      await vault.connect(user).disableYield(halfAmount);',
  '      expect(await vault.yieldEnabled(user.address)).to.be.true;',
  '    });',
  '',
  '    it("should revert when disabling more than enabled", async function () {',
  '      const amount = ethers.parseUnits("1000", 6);',
  '      await asset.connect(user).approve(await vault.getAddress(), amount);',
  '      await vault.connect(user).deposit(amount, user.address);',
  '      await vault.connect(user).enableYield(amount);',
  '      await mockAdapter.setSuppliedValue(amount);',
  '      await expect(',
  '        vault.connect(user).disableYield(amount + 1n)',
  '      ).to.be.revertedWithCustomError(vault, "InsufficientShares");',
  '    });',
  '  });',
  '',
  '  describe("Borrow and Repay", function () {',
  '    it("should allow borrow", async function () {',
  '      const amount = ethers.parseUnits("10000", 6);',
  '      await asset.connect(user).approve(await vault.getAddress(), amount);',
  '      await vault.connect(user).deposit(amount, user.address);',
  '      await vault.connect(user).enableYield(amount);',
  '      const borrowAmount = ethers.parseUnits("5000", 6);',
  '      await expect(() =>',
  '        vault.connect(borrower).borrow(borrowAmount, borrower.address)',
  '      ).to.changeTokenBalance(asset, borrower, borrowAmount);',
  '    });',
  '',
  '    it("should track debt correctly", async function () {',
  '      const amount = ethers.parseUnits("10000", 6);',
  '      await asset.connect(user).approve(await vault.getAddress(), amount);',
  '      await vault.connect(user).deposit(amount, user.address);',
  '      await vault.connect(user).enableYield(amount);',
  '      const borrowAmount = ethers.parseUnits("5000", 6);',
  '      await vault.connect(borrower).borrow(borrowAmount, borrower.address);',
