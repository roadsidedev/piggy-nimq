lines.push("  });");
lines.push("");
lines.push('  describe("Deployment", function () {');
lines.push('    it("should set the correct asset and admin", async function () {');
lines.push("      expect(await vault.asset()).to.equal(await asset.getAddress());");
lines.push("      expect(await vault.hasRole(await vault.DEFAULT_ADMIN_ROLE(), admin.address)).to.be.true;");
lines.push("    });");
lines.push('    it("should have zero balance initially", async function () {');
lines.push("      const vAddr = await vault.getAddress();");
lines.push("      expect(await asset.balanceOf(vAddr)).to.equal(0);");
lines.push("    });");
lines.push('    it("should revert reinitialization (UUPS)", async function () {');
lines.push('      const VF = await ethers.getContractFactory("PiggyVault");');
lines.push("      const vAddr = await vault.getAddress();");
lines.push("      const aAddr = await asset.getAddress();");
lines.push("      const maAddr = await mockAdapter.getAddress();");
lines.push("      await expect(");
lines.push("        upgrades.upgradeProxy(vAddr, VF, {");
lines.push('          kind: "uups",');
lines.push('          call: { fn: "initialize", args: [admin.address, operator.address, aAddr, maAddr, 5000, ethers.parseEther("1.3")] },');
lines.push("        })");
lines.push("      ).to.be.reverted;");
lines.push("    });");
lines.push("  });");
lines.push("");
lines.push('  describe("Deposits", function () {');
lines.push('    it("should allow deposits", async function () {');
lines.push('      const amount = ethers.parseUnits("1000", 6);');
lines.push("      const vAddr = await vault.getAddress();");
lines.push("      await asset.connect(user).approve(vAddr, amount);");
lines.push("      await vault.connect(user).deposit(amount);");
lines.push("      expect(await vault.idleBalance(user.address)).to.equal(amount);");
lines.push("    });");
lines.push('    it("should revert zero deposit", async function () {');
lines.push('      await expect(vault.connect(user).deposit(0)).to.be.revertedWithCustomError(vault, "ZeroAmount");');
lines.push("    });");
lines.push("  });");
lines.push("");
lines.push('  describe("enableYield", function () {');
lines.push('    it("should enable yield with dead shares protection", async function () {');
lines.push('      const amount = ethers.parseUnits("1000", 6);');
lines.push("      const vAddr = await vault.getAddress();");
lines.push("      await asset.connect(user).approve(vAddr, amount);");
lines.push("      await vault.connect(user).deposit(amount);");
lines.push("      await vault.connect(user).enableYield(amount);");
lines.push("      const DS = 1000n;");
lines.push("      expect(await vault.supplyShares(user.address)).to.equal(amount - DS);");
lines.push("      expect(await vault.supplyShares(ethers.ZeroAddress)).to.equal(DS);");
lines.push("    });");
lines.push('    it("should revert if amount <= DEAD_SHARES as first depositor", async function () {');
lines.push("      const vAddr = await vault.getAddress();");
lines.push("      await asset.connect(user).approve(vAddr, 1000);");
lines.push("      await vault.connect(user).deposit(1000);");
lines.push("      await expect(");
lines.push("        vault.connect(user).enableYield(999)");
lines.push('      ).to.be.revertedWithCustomError(vault, "InsufficientFirstDeposit");');
lines.push("    });");
lines.push('    it("should revert zero amount", async function () {');
lines.push("      await expect(");
lines.push("        vault.connect(user).enableYield(0)");
lines.push('      ).to.be.revertedWithCustomError(vault, "ZeroAmount");');
lines.push("    });");
lines.push('    it("should revert if insufficient balance", async function () {');
lines.push("      await expect(");
lines.push("        vault.connect(user).enableYield(100)");
lines.push('      ).to.be.revertedWithCustomError(vault, "InsufficientUnallocatedBalance");');
lines.push("    });");
lines.push("  });");
lines.push("");
lines.push('  describe("disableAllYield", function () {');
lines.push('    it("should disable yield and clear supply shares", async function () {');
lines.push('      const amount = ethers.parseUnits("1000", 6);');
lines.push("      const vAddr = await vault.getAddress();");
lines.push("      await asset.connect(user).approve(vAddr, amount);");
lines.push("      await vault.connect(user).deposit(amount);");
lines.push("      await vault.connect(user).enableYield(amount);");
lines.push("      // Mock adapter tracks supplied state through actual supplyToAave calls");
lines.push("      await vault.connect(user).disableAllYield();");
lines.push("      expect(await vault.supplyShares(user.address)).to.equal(0);");
lines.push("    });");
lines.push('    it("should revert if not enabled", async function () {');
lines.push("      await expect(");
lines.push("        vault.connect(other).disableAllYield()");
lines.push('      ).to.be.revertedWithCustomError(vault, "NoYieldPosition");');
lines.push("    });");
lines.push("  });");
lines.push("");
lines.push('  describe("disableYield", function () {');
lines.push('    it("should disable partial yield", async function () {');
lines.push('      const amount = ethers.parseUnits("1000", 6);');
lines.push("      const vAddr = await vault.getAddress();");
lines.push("      await asset.connect(user).approve(vAddr, amount);");
lines.push("      await vault.connect(user).deposit(amount);");
lines.push("      await vault.connect(user).enableYield(amount);");
lines.push("      await vault.connect(user).disableYield(amount / 2n);");
lines.push("      expect(await vault.supplyShares(user.address)).to.be.gt(0);");
lines.push("      expect(await vault.supplyShares(user.address)).to.be.lt(amount);");
lines.push("    });");
lines.push('    it("should revert if over-withdrawing yield", async function () {');
lines.push('      const amount = ethers.parseUnits("1000", 6);');
lines.push("      const vAddr = await vault.getAddress();");
lines.push("      await asset.connect(user).approve(vAddr, amount);");
lines.push("      await vault.connect(user).deposit(amount);");
lines.push("      await vault.connect(user).enableYield(amount);");
lines.push("      await expect(");
lines.push("        vault.connect(user).disableYield(amount + 1n)");
