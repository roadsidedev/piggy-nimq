lines.push("    await _asset.mint(_admin.address, mintAmt);");
lines.push("");
lines.push('    const F2 = await ethers.getContractFactory("MockAaveAdapter");');
lines.push("    const _mockAdapter = await F2.deploy();");
lines.push("    await _mockAdapter.waitForDeployment();");
lines.push("    await _mockAdapter.setAsset(assetAddr);");
