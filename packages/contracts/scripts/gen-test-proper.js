lines.push('      ).to.be.revertedWithCustomError(vault, "AccessControlUnauthorizedAccount");');
lines.push("    });");
lines.push('    it("should reject non-admin setTotalDepositCap", async function () {');
lines.push("      await expect(");
lines.push("        vault.connect(user).setTotalDepositCap(1000)");
lines.push('      ).to.be.revertedWithCustomError(vault, "AccessControlUnauthorizedAccount");');
lines.push("    });");
lines.push("  });");
